// Generates src/io/embeddedAssets.ts — the legacy export & preview features read these data
// files from the plugin folder via fs, but community installs only ship main.js/manifest/
// styles. We base64-embed the (~21KB) assets so main.js can unpack them on load.
// Run automatically by `npm run build` / `npm run dev`.
import { promises as fs } from "node:fs";
import * as path from "node:path";

const ASSETS = [
  "blank.hwpx",
  "hwp-assets/reference-hwpx/default.hwpx",
  "hwp-assets/templates/default.json",
  "word-assets/Convert-WordToPdf.ps1",
  "word-assets/docx_style_map.lua"
];

const root = process.cwd();
let out =
  "// AUTO-GENERATED by scripts/gen-embedded-assets.mjs — do not edit by hand.\n" +
  "// Runtime assets, base64-embedded into main.js and unpacked into the plugin folder on load.\n" +
  "export interface EmbeddedAsset {\n  rel: string;\n  b64: string;\n}\n\n" +
  "export const EMBEDDED_ASSETS: EmbeddedAsset[] = [\n";

let total = 0;
for (const rel of ASSETS) {
  const abs = path.join(root, rel.split("/").join(path.sep));
  const buf = await fs.readFile(abs);
  total += buf.byteLength;
  out += `  { rel: ${JSON.stringify(rel)}, b64: ${JSON.stringify(buf.toString("base64"))} },\n`;
}
out += "];\n";

await fs.mkdir(path.join(root, "src", "io"), { recursive: true });
await fs.writeFile(path.join(root, "src", "io", "embeddedAssets.ts"), out, "utf8");
console.log(`Embedded ${ASSETS.length} assets (${(total / 1024).toFixed(1)} KB) → src/io/embeddedAssets.ts`);
